    public void cargar_lista_de_tareas(){
        try{            
            cnx = Conexion.obtener(); 
            if (cnx!=null)
            {
                this.prest = this.servicios.recuperarTodas(Conexion.obtener());
                DefaultTableModel dtm = (DefaultTableModel) TablaPrestamoL.getModel();
                dtm.setRowCount(0);
                for(int i = 0; i < this.prest.size(); i++){
                dtm.addRow(new Object[]{
                    this.prest.get(i).getCodigo_Prestamo(),                   
                    this.prest.get(i).getMonto(),  
                    this.prest.get(i).getDuracion(),
                    this.prest.get(i).getTipoPrestamo()                     
                });
            }
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
   
