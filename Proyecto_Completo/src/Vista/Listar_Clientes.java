    public void cargar_lista_de_tareas(){
        try{            
            cnx = Conexion.obtener(); 
            if (cnx!=null)
            {
                this.cliente = this.servicios.recuperarTodas(Conexion.obtener());
                DefaultTableModel dtm = (DefaultTableModel) TablaClienteListar.getModel();
                dtm.setRowCount(0);
                for(int i = 0; i < this.cliente.size(); i++){
                dtm.addRow(new Object[]{
                    this.cliente.get(i).getCedula(),                    
                    this.cliente.get(i).getApellidos(),
                    this.cliente.get(i).getNombres(),
                    this.cliente.get(i).getDireccion(),
                    this.cliente.get(i).getTelefono(),
                    this.cliente.get(i).getIng_Mensual(),
                    this.cliente.get(i).getTipoem()                     
                });
            }
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
    
